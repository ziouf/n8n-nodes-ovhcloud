import type {
	IDataObject,
	IDisplayOptions,
	IExecuteFunctions,
	INodeExecutionData,
	INodeProperties,
} from 'n8n-workflow';
import { ApiClient } from '../../../../transport/ApiClient';

/**
 * @brief List Highlighted Extensions operation
 *
 * Lists highlighted domain extensions.
 *
 * HTTP method: GET
 * Endpoint: /domain/extensions/highlighted
 */
// eslint-disable-next-line @typescript-eslint/no-unused-vars
export function description(displayOptions: IDisplayOptions): INodeProperties[] {
	return [];
}

/**
 * Executes the List Highlighted Extensions operation.
 */
export async function execute(this: IExecuteFunctions): Promise<INodeExecutionData[]> {
	const client = new ApiClient(this);
	const data = (await client.httpGet('/domain/extensions/highlighted')) as IDataObject[];
	return this.helpers.returnJsonArray(data);
}
