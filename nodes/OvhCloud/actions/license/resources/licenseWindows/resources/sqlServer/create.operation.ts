import type {
	IExecuteFunctions,
	INodeExecutionData,
	IDataObject,
	INodeProperties,
	IDisplayOptions,
} from 'n8n-workflow';
import { ApiClient } from '../../../../../../transport/ApiClient';

/**
 * Add SQL Server to a Windows license.
 *
 * HTTP method: POST
 * Endpoint: /license/windows/{serviceName}/sqlServer
 */
export function description(displayOptions: IDisplayOptions): INodeProperties[] {
	return [
		{
			displayName: 'Service Name',
			name: 'serviceName',
			type: 'string',
			default: '',
			required: true,
			description: 'The name of the Windows license service',
			displayOptions,
		},
	];
}

/**
 * Executes the Add SQL Server operation.
 */
export async function execute(this: IExecuteFunctions): Promise<INodeExecutionData[]> {
	const client = new ApiClient(this);
	const serviceName = this.getNodeParameter('serviceName', 0) as string;
	const data = (await client.httpPost(
		`/license/windows/${serviceName}/sqlServer`,
	)) as IDataObject;
	return [{ json: data }];
}
