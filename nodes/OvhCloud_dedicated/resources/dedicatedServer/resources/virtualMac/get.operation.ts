import type {
	IDataObject,
	IDisplayOptions,
	IExecuteFunctions,
	INodeExecutionData,
	INodeProperties,
} from 'n8n-workflow';
import { ApiClient } from '../../../../../../shared/transport/ApiClient';

/**
 * @brief Get Virtual MAC operation for Dedicated Server
 *
 * Retrieves details of a specific virtual MAC.
 *
 * HTTP method: GET
 * Endpoint: /dedicated/server/{serviceName}/virtualMac/{macAddress}
 */
export function description(displayOptions: IDisplayOptions): INodeProperties[] {
	return [
		{
			displayName: 'Service Name',
			name: 'serviceName',
			description:
				'The name of the dedicated server. This can be set manually or selected from the list of services.',
			type: 'resourceLocator',
			required: true,
			default: { mode: 'str', value: '' },
			modes: [
				{ displayName: 'By Name', name: 'str', type: 'string' },
				{
					displayName: 'From List',
					name: 'list',
					type: 'list',
					placeholder: 'Select a dedicated server...',
					typeOptions: { searchListMethod: 'getDedicatedServerServices', searchable: true },
				},
			],
			displayOptions,
		},
		{
			displayName: 'MAC Address',
			name: 'macAddress',
			type: 'string',
			default: '',
			required: true,
			description: 'The MAC address to retrieve',
			displayOptions,
		},
	];
}

export async function execute(this: IExecuteFunctions): Promise<INodeExecutionData[]> {
	const client = new ApiClient(this);
	const serviceName = this.getNodeParameter('serviceName', 0, { extractValue: true }) as string;
	const macAddress = this.getNodeParameter('macAddress', 0) as string;
	const data = (await client.httpGet(`/dedicated/server/${serviceName}/virtualMac/${macAddress}`)) as IDataObject;
	return [{ json: data }];
}
